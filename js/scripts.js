//Gallons to ounces
var oz = parseInt(prompt("How many gallons?"));
var oz = oz * 128;
alert(oz + " Ounces.");

//Gallons to cups
var cups = parseInt(prompt("How many gallons?"));
var cups = cups * 16;
alert(cups + " Cups.");

//Cups to tablespoons
var tbsp = parseInt(prompt("How many cups?"));
var tbsp = tbsp * 16;
alert(tbsp + " tablespoons.");

//Gallons to tablespoons
var tbsp = parseInt(prompt("How many gallons?"));
var tbsp = tbsp * 256;
alert(tbsp + " tablespoons.");
